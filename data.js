function placeholder(label, bg, fg) {
  bg = bg || "1a2030"; fg = fg || "3d7fff";
  const svg = `<svg xmlns="http://www.w3.org/2000/svg" width="800" height="600">
    <rect width="800" height="600" fill="#${bg}"/>
    <g stroke="#${fg}" stroke-opacity="0.25">
      <line x1="0" y1="150" x2="800" y2="150"/><line x1="0" y1="300" x2="800" y2="300"/>
      <line x1="0" y1="450" x2="800" y2="450"/><line x1="200" y1="0" x2="200" y2="600"/>
      <line x1="400" y1="0" x2="400" y2="600"/><line x1="600" y1="0" x2="600" y2="600"/>
    </g>
    <text x="50%" y="50%" fill="#${fg}" font-family="monospace" font-size="28"
      text-anchor="middle" dominant-baseline="middle">${label}</text>
  </svg>`;
  return "data:image/svg+xml;utf8," + encodeURIComponent(svg);
}

